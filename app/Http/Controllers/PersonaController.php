    public function buscarPersona(Request $request){
        // require 'vendor/autoload.php';
        $dni = $request->dni;
        $cs = new Dni();
        $cs->setClient(new ContextClient());

        $person = $cs->get($dni);
        if ($person === false) {
            echo $cs->getError();
            exit();
        }

        return json_encode($person);
    }
